public class LetraU {
    public static char[][] letraU(char caracter) {
        char[][] arr = {
                {caracter, ' ', ' ', ' ', caracter},
                {caracter, ' ', ' ', ' ', caracter},
                {caracter, ' ', ' ', ' ', caracter},
                {caracter, ' ', ' ', ' ', caracter},
                {caracter, caracter, caracter, caracter, caracter}
        };
        return arr;
    }
}
